// Helper functions
export const getTaskById = (taskId) => {
  return tasks.find(task => task.taskId === taskId)
}

export const getTasksByStatus = (status) => {
  return tasks.filter(task => task.status === status)
}

export const getTasksByAssignee = (staffNumber) => {
  return tasks.filter(task => task.assignees.includes(staffNumber))
}

export const getTasksBySprint = (sprintId, sprints) => {
  const sprint = sprints.find(s => s.sprintId === sprintId)
  return sprint ? tasks.filter(t => sprint.tasks.includes(t.taskId)) : []
}

export const getTasksByBacklog = (backlogId, backlogs) => {
  const backlog = backlogs.find(b => b.backlogId === backlogId)
  return backlog ? tasks.filter(t => backlog.tasks.includes(t.taskId)) : []
}
