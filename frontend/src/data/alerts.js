// Helper functions
export const getAlertById = (alertId) => {
  return alerts.find(alert => alert.alertId === alertId)
}

export const getAlertsByUser = (userId) => {
  return alerts.filter(alert => alert.userId === userId)
}

export const getAlertsByProject = (projectId) => {
  return alerts.filter(alert => alert.projectId === projectId)
}

export const getUnreadAlerts = (userId) => {
  return alerts.filter(alert => alert.userId === userId && alert.status === 'unread')
}

export const getAlertsByPriority = (priority) => {
  return alerts.filter(alert => alert.priority === priority)
}

export const getRecentAlerts = (userId, count = 10) => {
  return alerts
    .filter(alert => alert.userId === userId)
    .sort((a, b) => new Date(b.dateCreated) - new Date(a.dateCreated))
    .slice(0, count)
}

export const markAlertAsRead = (alertId) => {
  const alert = alerts.find(a => a.alertId === alertId)
  if (alert) {
    alert.status = 'read'
  }
  return alert
}
