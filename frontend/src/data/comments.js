// Helper functions
export const getCommentById = (commentId) => {
  return comments.find(comment => comment.commentId === commentId)
}

export const getCommentsByTask = (taskId, tasks) => {
  const task = tasks.find(t => t.taskId === taskId)
  return task ? comments.filter(c => task.comments.includes(c.commentId)) : []
}

export const getCommentsByUser = (staffNumber) => {
  return comments.filter(comment => comment.postedBy === staffNumber)
}

export const getRecentComments = (count = 10) => {
  return comments
    .sort((a, b) => new Date(b.timestamp) - new Date(a.timestamp))
    .slice(0, count)
}
