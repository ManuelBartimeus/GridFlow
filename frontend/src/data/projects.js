// Helper functions
export const getProjectById = (projectId) => {
  return projects.find(proj => proj.projectId === projectId)
}

export const getProjectsByDepartment = (department) => {
  return projects.filter(proj => proj.department === department)
}

export const getProjectsByStatus = (status) => {
  return projects.filter(proj => proj.currentStatus === status)
}

export const getProjectsByPriority = (priority) => {
  return projects.filter(proj => proj.priority === priority)
}

export const getProjectsByMember = (staffNumber) => {
  return projects.filter(proj => 
    proj.projectMembers.includes(staffNumber) ||
    proj.projectOwner.includes(staffNumber) ||
    proj.projectManager.includes(staffNumber) ||
    proj.teamLead.includes(staffNumber)
  )
}

export const getLinkedProjects = (projectId) => {
  return projects.filter(proj => proj.projectLinkages.includes(projectId))
}
